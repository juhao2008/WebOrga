package com.orga.domain;

public class Parent {
	private String parentNumber;
	private Student student;
	private String parentName;
	private String relation;
	private String parentProfession;
	private String parentWork;
	private String parentTelephone;
	private String parentEmail;
	private String parentQQ;
	private String parentAddress;
	private String parentMemo;
	
	public String getParentNumber() {
		return parentNumber;
	}
	public void setParentNumber(String parentNumber) {
		this.parentNumber = parentNumber;
	}
	public Student getStudent() {
		return student;
	}
	public void setStudent(Student student) {
		this.student = student;
	}
	public String getParentName() {
		return parentName;
	}
	public void setParentName(String parentName) {
		this.parentName = parentName;
	}
	public String getRelation() {
		return relation;
	}
	public void setRelation(String relation) {
		this.relation = relation;
	}
	public String getParentProfession() {
		return parentProfession;
	}
	public void setParentProfession(String parentProfession) {
		this.parentProfession = parentProfession;
	}
	public String getParentWork() {
		return parentWork;
	}
	public void setParentWork(String parentWork) {
		this.parentWork = parentWork;
	}
	public String getParentTelephone() {
		return parentTelephone;
	}
	public void setParentTelephone(String parentTelephone) {
		this.parentTelephone = parentTelephone;
	}
	public String getParentEmail() {
		return parentEmail;
	}
	public void setParentEmail(String parentEmail) {
		this.parentEmail = parentEmail;
	}
	public String getParentQQ() {
		return parentQQ;
	}
	public void setParentQQ(String parentQQ) {
		this.parentQQ = parentQQ;
	}
	public String getParentAddress() {
		return parentAddress;
	}
	public void setParentAddress(String parentAddress) {
		this.parentAddress = parentAddress;
	}
	public String getParentMemo() {
		return parentMemo;
	}
	public void setParentMemo(String parentMemo) {
		this.parentMemo = parentMemo;
	}
	
}
